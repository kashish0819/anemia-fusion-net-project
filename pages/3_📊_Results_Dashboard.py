# ----------------------------------------
# Demo Data
# (Later this will come from Session State)
# ----------------------------------------

prediction = "Anemia"

confidence = 92.4

risk = "High"

state = "Gujarat"

geo_score = 0.82

patient_name = "Patient"

gender = "Male"

age = 25

# ----------------------------------------
# Top Cards
# ----------------------------------------

c1, c2, c3, c4 = st.columns(4)

with c1:
    st.metric(
        "Prediction",
        prediction
    )

with c2:
    st.metric(
        "Confidence",
        f"{confidence:.2f}%"
    )

with c3:
    st.metric(
        "Risk Level",
        risk
    )

with c4:
    st.metric(
        "Geo Score",
        f"{geo_score:.2f}"
    )

st.divider()

# ----------------------------------------
# Dashboard Overview
# ----------------------------------------

left, right = st.columns([2, 1])

with left:

    st.subheader("🧑 Patient Information")

    st.write(f"**Name:** {patient_name}")
    st.write(f"**Age:** {age}")
    st.write(f"**Gender:** {gender}")
    st.write(f"**State:** {state}")
    st.write(f"**Date:** {datetime.today().strftime('%d-%m-%Y')}")

with right:

    fig = go.Figure(go.Indicator(
        mode="gauge+number",
        value=confidence,
        title={"text": "AI Confidence"},
        gauge={
            "axis": {"range": [0, 100]},
            "bar": {"color": "#2563EB"},
            "steps": [
                {"range": [0, 50], "color": "#FEE2E2"},
                {"range": [50, 75], "color": "#FEF3C7"},
                {"range": [75, 100], "color": "#DCFCE7"}
            ]
        }
    ))

    fig.update_layout(height=300)

    st.plotly_chart(
        fig,
        use_container_width=True
    )

st.divider()